async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    context.user_data.clear()
    await update.message.reply_text(
        "👋 Добро пожаловать в систему обучения *Booster*!\n\n"
        "Здесь ты изучишь всё необходимое для работы менеджером и пройдёшь тестирование.\n\n"
        "Выбери с чего начать:",
        parse_mode="Markdown",
        reply_markup=get_main_keyboard()
    )

async def button_handler(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    await query.answer()
    data = query.data

    # Главное меню
    if data == "main_menu":
        context.user_data.clear()
        await query.edit_message_text(
            "👋 Добро пожаловать в систему обучения *Booster*!\n\n"
            "Выбери с чего начать:",
            parse_mode="Markdown",
            reply_markup=get_main_keyboard()
        )

    # Меню обучения
    elif data == "menu_training":
        await query.edit_message_text(
            "📚 *Выбери модуль для изучения:*\n\n"
            "Пройди все модули последовательно для полного понимания работы.",
            parse_mode="Markdown",
            reply_markup=get_modules_keyboard()
        )

    # Страница модуля
    elif data.startswith("module_") and "_page_" in data:
        parts = data.split("_")
        mod_id = int(parts[1])
        page = int(parts[3])
        module = MODULES[mod_id]

        if module.get("content_type") == "situations":
            await query.edit_message_text(
                "👥 *Типы клиентов и ситуации на встрече*\n\n"
                "Выбери ситуацию, прочитай описание и выбери правильный ответ:",
                parse_mode="Markdown",
                reply_markup=get_situations_list_keyboard()
            )
        else:
            pages = module["pages"]
            total = len(pages)
            text = pages[page]
            progress = f"\n\n📄 Страница {page+1}/{total}"
            await query.edit_message_text(
                text + progress,
                parse_mode="Markdown",
                reply_markup=get_page_keyboard(mod_id, page, total)
            )

    # Начало ситуации
    elif data.startswith("sit_") and data.endswith("_start"):
        sit_id = int(data.split("_")[1])
        situation = next(s for s in SITUATIONS if s["id"] == sit_id)
        options_text = "\n".join([f"*{l}.* {t}" for l, t, _ in situation["options"]])
        text = f"🎭 *{situation['title']}*\n\n{situation['description']}\n\n{options_text}\n\n👇 Выбери ответ:"
        await query.edit_message_text(
            text, parse_mode="Markdown",
            reply_markup=get_situation_keyboard(sit_id)
        )

    # Ответ на ситуацию
    elif data.startswith("sit_") and "_ans_" in data:
        parts = data.split("_")
        sit_id = int(parts[1])
        selected = parts[3]
        situation = next(s for s in SITUATIONS if s["id"] == sit_id)
        correct = [l for l, t, is_correct in situation["options"] if is_correct]
        is_right = selected in correct
        result = "✅ Верно!" if is_right else f"❌ Неверно. Правильный ответ: {', '.join(correct)}"
        text = f"{result}\n\n{situation['explanation']}"
        keyboard = InlineKeyboardMarkup([
            [InlineKeyboardButton("📋 К списку ситуаций", callback_data=f"module_7_page_0")],
            [InlineKeyboardButton("🏠 Главное меню", callback_data="main_menu")]
        ])
        await query.edit_message_text(text, parse_mode="Markdown", reply_markup=keyboard)

    # Следующая ситуация
    elif data.startswith("sit_next_"):
        sit_id = int(data.split("_")[2])
        next_id = sit_id + 1 if sit_id < len(SITUATIONS) else 1
        situation = next(s for s in SITUATIONS if s["id"] == next_id)
        options_text = "\n".join([f"*{l}.* {t}" for l, t, _ in situation["options"]])
        text = f"🎭 *{situation['title']}*\n\n{situation['description']}\n\n{options_text}\n\n👇 Выбери ответ:"
        await query.edit_message_text(
            text, parse_mode="Markdown",
            reply_markup=get_situation_keyboard(next_id)
        )

    # Досрочное завершение теста
    elif data == "test_finish":
        await show_test_results(query, context)

    # Начало теста
    elif data == "menu_test":
        questions = random.sample(ALL_QUESTIONS, min(100, len(ALL_QUESTIONS)))
        context.user_data["test_questions"] = questions
        context.user_data["test_index"] = 0
        context.user_data["test_answers"] = []
        await send_test_question(query, context)

    # Ответ на вопрос теста
    elif data.startswith("test_ans_"):
        pos_idx = int(data.split("_")[2])
        questions = context.user_data.get("test_questions", [])
        idx = context.user_data.get("test_index", 0)
        answers = context.user_data.get("test_answers", [])

        if idx < len(questions):
            q = questions[idx]
            # Переводим позицию кнопки в оригинальный индекс
            shuffled = context.user_data.get("shuffled", {}).get(idx, list(range(len(q["options"]))))
            orig_ans_idx = shuffled[pos_idx]
            is_correct = (orig_ans_idx == q["answer"])
            # Для отображения ошибок сохраняем оригинальные индексы
            answers.append({
                "q": q["q"],
                "selected": orig_ans_idx,
                "correct": q["answer"],
                "options": q["options"],
                "is_correct": is_correct
            })
            context.user_data["test_answers"] = answers
            context.user_data["test_index"] = idx + 1

            if idx + 1 >= len(questions):
                await show_test_results(query, context)
            else:
                await send_test_question(query, context)

async def send_test_question(query, context):
    questions = context.user_data["test_questions"]
    idx = context.user_data["test_index"]
    q = questions[idx]
    total = len(questions)
    correct_so_far = sum(1 for a in context.user_data["test_answers"] if a["is_correct"])

    # Перемешиваем варианты, запоминаем новый порядок
    shuffled_indices = list(range(len(q["options"])))
    random.shuffle(shuffled_indices)
    
    # Сохраняем порядок для правильной проверки ответа
    if "shuffled" not in context.user_data:
        context.user_data["shuffled"] = {}
    context.user_data["shuffled"][idx] = shuffled_indices

    keyboard = []
    for pos, orig_i in enumerate(shuffled_indices):
        opt = q["options"][orig_i]
        keyboard.append([InlineKeyboardButton(f"{chr(65+pos)}. {opt}", callback_data=f"test_ans_{pos}")])
    keyboard.append([InlineKeyboardButton(f"🏁 Завершить тест ({idx}/{total} отвечено)", callback_data="test_finish")])

    progress = f"Вопрос {idx+1}/{total} | ✅ {correct_so_far}"
    text = f"*{progress}*\n\n❓ {q['q']}"

    await query.edit_message_text(
        text, parse_mode="Markdown",
        reply_markup=InlineKeyboardMarkup(keyboard)
    )

async def show_test_results(query, context):
    answers = context.user_data["test_answers"]
    total = len(answers)
    correct = sum(1 for a in answers if a["is_correct"])
    score_pct = round(correct / total * 100)
    passed = score_pct >= 80

    result_emoji = "🎉" if passed else "😔"
    result_text = "ТЕСТ ПРОЙДЕН!" if passed else "ТЕСТ НЕ ПРОЙДЕН"

    # Сбор ошибок
    errors = [a for a in answers if not a["is_correct"]]

    text = (
        f"{result_emoji} *{result_text}*\n\n"
        f"Результат: *{correct}/{total}* ({score_pct}%)\n"
        f"Проходной балл: 80%\n\n"
    )

    if errors:
        text += f"❌ *Ошибки ({len(errors)} вопросов):*\n\n"
        for i, err in enumerate(errors[:30], 1):  # Показываем первые 30
            right_opt = err["options"][err["correct"]]
            selected_opt = err["options"][err["selected"]]
            text += f"{i}. {err['q']}\n   ❌ Ты: {selected_opt}\n   ✅ Правильно: {right_opt}\n\n"

        if len(errors) > 30:
            text += f"...и ещё {len(errors)-30} ошибок\n\n"

    if not passed:
        text += "📸 Сделай скрин результата и пересдай тест после изучения ошибок."
    else:
        text += "📸 Сделай скрин результата."

    keyboard = InlineKeyboardMarkup([
        [InlineKeyboardButton("🔄 Пройти снова", callback_data="menu_test")],
        [InlineKeyboardButton("📚 Вернуться к обучению", callback_data="menu_training")],
        [InlineKeyboardButton("🏠 Главное меню", callback_data="main_menu")]
    ])

    # Если текст слишком длинный — разбиваем
    if len(text) > 4000:
        text = text[:3900] + "\n\n...список обрезан. Сделай скрин и изучи ошибки."

    await query.edit_message_text(
        text, parse_mode="Markdown",
        reply_markup=keyboard
    )

