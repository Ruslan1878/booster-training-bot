def get_main_keyboard():
    keyboard = [
        [InlineKeyboardButton("📚 Обучение", callback_data="menu_training")],
        [InlineKeyboardButton("✅ Пройти тестирование", callback_data="menu_test")]
    ]
    return InlineKeyboardMarkup(keyboard)

def get_modules_keyboard():
    keyboard = []
    for mod_id, mod in MODULES.items():
        keyboard.append([InlineKeyboardButton(mod["title"], callback_data=f"module_{mod_id}_page_0")])
    keyboard.append([InlineKeyboardButton("🏠 Главное меню", callback_data="main_menu")])
    return InlineKeyboardMarkup(keyboard)

def get_page_keyboard(mod_id, page, total_pages):
    keyboard = []
    nav = []
    if page > 0:
        nav.append(InlineKeyboardButton("◀️ Назад", callback_data=f"module_{mod_id}_page_{page-1}"))
    if page < total_pages - 1:
        nav.append(InlineKeyboardButton("Далее ▶️", callback_data=f"module_{mod_id}_page_{page+1}"))
    if nav:
        keyboard.append(nav)
    keyboard.append([InlineKeyboardButton("📋 К модулям", callback_data="menu_training")])
    keyboard.append([InlineKeyboardButton("🏠 Главное меню", callback_data="main_menu")])
    return InlineKeyboardMarkup(keyboard)

def get_situation_keyboard(sit_id, show_answer=False, selected=None):
    keyboard = []
    if not show_answer:
        row = []
        for letter, text, _ in next(s for s in SITUATIONS if s["id"] == sit_id)["options"]:
            row.append(InlineKeyboardButton(f"{letter}", callback_data=f"sit_{sit_id}_ans_{letter}"))
        keyboard.append(row)
    else:
        keyboard.append([InlineKeyboardButton("➡️ Следующая ситуация", callback_data=f"sit_next_{sit_id}")])
    keyboard.append([InlineKeyboardButton("📋 К модулям", callback_data="menu_training")])
    return InlineKeyboardMarkup(keyboard)

def get_situations_list_keyboard():
    keyboard = []
    for i, sit in enumerate(SITUATIONS):
        keyboard.append([InlineKeyboardButton(f"{i+1}. {sit['title']}", callback_data=f"sit_{sit['id']}_start")])
    keyboard.append([InlineKeyboardButton("🏠 Главное меню", callback_data="main_menu")])
    return InlineKeyboardMarkup(keyboard)

